package com.mingtech.application.pool.bank.hkb;
/**
 *返回数组类型的对象
 */
public class Retmes {
	private String ORGCODE;//客户号
	private String KHMC;//客户名称
	private String KHJS;//客户角色
	public Retmes() {
		super();
	}
	public String getORGCODE() {
		return ORGCODE;
	}
	public void setORGCODE(String oRGCODE) {
		ORGCODE = oRGCODE;
	}
	public String getKHMC() {
		return KHMC;
	}
	public void setKHMC(String kHMC) {
		KHMC = kHMC;
	}
	public String getKHJS() {
		return KHJS;
	}
	public void setKHJS(String kHJS) {
		KHJS = kHJS;
	}
	

	
}
